void ACppWorldCharacter::MoveForward(float Value)
{
	if (Value != 0.0f)
	{
		// add movement in that direction
		AddMovementInput(GetActorForwardVector(), Value);
	}
}

void ACppWorldCharacter::MoveRight(float Value)
{
	if (Value != 0.0f)
	{
		// add movement in that direction
		AddMovementInput(GetActorRightVector(), Value);
	}
}

void ACppWorldCharacter::TurnAtRate(float Rate)
{
	// calculate delta for this frame from the rate information
	AddControllerYawInput(Rate * BaseTurnRate * GetWorld()->GetDeltaSeconds());
}

void ACppWorldCharacter::LookUpAtRate(float Rate)
{
	// calculate delta for this frame from the rate information
	AddControllerPitchInput(Rate * BaseLookUpRate * GetWorld()->GetDeltaSeconds());
}

bool ACppWorldCharacter::EnableTouchscreenMovement(class UInputComponent* PlayerInputComponent)
{
	if (FPlatformMisc::SupportsTouchInput() || GetDefault<UInputSettings>()->bUseMouseForTouch)
	{
		PlayerInputComponent->BindTouch(EInputEvent::IE_Pressed, this, &ACppWorldCharacter::BeginTouch);
		PlayerInputComponent->BindTouch(EInputEvent::IE_Released, this, &ACppWorldCharacter::EndTouch);

		//Commenting this out to be more consistent with FPS BP template.
		//PlayerInputComponent->BindTouch(EInputEvent::IE_Repeat, this, &ACppWorldCharacter::TouchUpdate);
		return true;
	}
	
	return false;
}


//defining OnAction() checks if current light switch is null, if not, then when the player presses the action key, the light will toggle.

void ACppWorldCharacter::OnAction()
{
	if (CurrentLightSwitch)
	{
		CurrentLightSwitch->ToggleLight();
	}
}

//defining overlapbegin function. if the otheractor is overlapped, (and otheractor is not the trigger capsule), and othercomp is overlapped, and otheractor is derived from ALightSwitch, cast otheractor to current light switch on action. Making the pointer not null, toggling the light on.
void ACppWorldCharacter::OnOverlapBegin(class UPrimitiveComponent* OverlappedComp, class AActor* OtherActor, class UPrimitiveComponent* OtherComp, int32 OtherBodyIndex, bool bFromSweep, const FHitResult& SweepResult)
{
	if (OtherActor && (OtherActor != this) && OtherComp && OtherActor->GetClass()->IsChildOf(ALightSwitch::StaticClass()))
	{
		CurrentLightSwitch = Cast<ALightSwitch>(OtherActor);
	}
}

//defining overlap end function. if other actor (and other actor is not the trigger capsule) and other comp, make current light switch a null pointer on action.
void ACppWorldCharacter::OnOverlapEnd(class UPrimitiveComponent* OverlappedComp, class AActor* OtherActor, class UPrimitiveComponent* OtherComp, int32 OtherBodyIndex)
{
	if (OtherActor && (OtherActor != this) && OtherComp)
	{
		CurrentLightSwitch = nullptr;
	}
}

